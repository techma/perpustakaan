<h1 style="margin-left:175px; margin-right:200px">Profil Perpustakaan FMIPA UNS</h1>
<p style="text-align:justify; font-size:18px; margin-left:175px; margin-right:200px">Fakultas Matematika dan Ilmu Pengetahuan Alam (FMIPA) Universitas Sebelas Maret (UNS) di Surakarta berdiri berdasarkan Surat Keputusan
Mendikbud tertanggal 1 Oktober 1996 No : 0297/O/1996 dan diresmikan pada tanggal 2 Mei 1997 yang terdiri dari empat jurusan, yaitu Matematika,
Kimia, Biologi dan Fisika. Seiring dengan berdirinya Fakultas MIPA, Perpustakaan mulai dirintis keberadaannya, guna mendukung kelancaran kegiatan
belajar mengajar.</p>

<p style="text-align:justify; font-size:18px; margin-left:175px; margin-right:200px">Perpustakaan MIPA pertama kali menempati sebuah ruang dengan luas 6,5 m x 13 m di Lantai II Gedung A Fakultas MIPA UNS, dengan jumlah buku/ koleksi
awal &plusmn 1950 eksemplar. Koleksi terdiri dari buku, majalah/ jurnal, proceeding dan laporan penelitian. Seiring dengan perkembangan lembaga induknya,
perpustakaan menempati lokasi yang berpindah-pindah. Pada tahun 2005 Perpustakaan pindah ke Lantai I gedung A FMIPA UNS, Tahun 2007 pindah ke
Gedung B Lantai IV dan tahun 2010 Pindah Di lantai 2 Gedung C FMIPA UNS sampai sekarang (tahun 2013).</p>

